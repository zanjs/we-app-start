export default {
  /**
   * 打印普通信息
   * @param {any} interfac
   */
  Log(interfac) {
    console.log(interfac)
  },
  /**
   * 打印警告信息
   * @param {any} interfac
   */
  Warn(interfac) {
    console.warn(interfac)
  },
  /**
   * 打印错误信息
   * @param {any} interfac
   */
  Error(interfac) {
    console.error(interfac)
  },
}